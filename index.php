<div class="site8">
    <h1>Video card</h1>
    <img src="video.jpg" alt="">
    <h4>Karta grafike është përgjegjëse për atë që shihni në monitor. Shumica e kompjuterëve kanë një GPU (njësi grafike të përpunimit) të ndërtuar në motherboard, në vend që të ketë një kartë të veçantë video. Nëse ju pëlqen të luani lojëra grafike-intensive, ju mund të shtoni një video më të mirë.</h4>
</div>
<div class="site9">
    <h1>Sound card</h1>
    <img src="sound.jpg" width="300px" height="300px" alt="">
    <h4>Karta e zërit - e quajtur edhe një kartë audio - është përgjegjës për atë që dëgjoni në altoparlantë ose kufje. Shumica e motherboards kanë tinguj të integruar, por ju mund të përmirësoni në një kartë zanore për tinguj me cilësi të lartë.</h4>
</div>
<div class="site10">
    <h1>Network card</h1>
    <img src="net.jpg" alt="">
    <h4>Karta e rrjetit lejon kompjuterin tuaj të komunikojë nëpërmjet një rrjeti dhe të ketë qasje në internet. Mund të lidhet me një kabllo Ethernet ose nëpërmjet një lidhjeje wireless (shpesh të quajtur Wi-Fi). Shumë nga motherboards kanë lidhje të rrjetit të integruar, dhe një kartë rrjeti mund të shtohet në një vend të zgjerimit.</h4>
