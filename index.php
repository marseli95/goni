<div class="site4">
    <h1>CPU/processor</h1>
    <img src="cpu.jpg" alt="">
    <h4>Njësia qendrore e përpunimit (CPU), e quajtur edhe procesori, gjendet brenda në rastin e kompjuterit në motherboard. Nganjëherë quhet truri i kompjuterit. Sa herë që shtypni një çelës, klikoni miun, ose nisni një aplikacion, po dërgoni udhëzime në CPU.

CPU është zakonisht një shesh qeramike me dy inç me një çip silikon të vendosur brenda. Çipi është zakonisht për madhësinë e një tabloje. CPU përshtatet priza e CPU, e cila është e mbuluar nga lavamani, një objekt që thith ngrohjen nga CPU.

Shpejtësia e një procesori matet në megahertz (MHz) ose miliona instruksione për sekondë; dhe gigaherz (GHz), ose miliarda instruksione për sekondë. Një procesor më i shpejtë mund të ekzekutojë udhëzimet më shpejt. Megjithatë, shpejtësia aktuale e kompjuterit varet nga shpejtësia e shumë komponentëve të ndryshëm.</h4>
</div>
<div class="site5">
    <h1>RAM (random access memory)</h1>
    <img src="ram.jpg" alt="">
    <h4>RAM është memorie afatshkurtër e sistemit tuaj. Sa herë që kompjuteri juaj kryen llogaritje, ai përkohësisht i ruan të dhënat në RAM derisa të jetë e nevojshme.

Ky memorie afatshkurtër zhduket kur kompjuteri është i fikur. Nëse jeni duke punuar në një dokument, spreadsheet ose një lloj tjetër skedari, do t'ju duhet ta ruani. Kur ruani një skedar, të dhënat shkruhen në hard drive, e cila vepron si ruajtje afatgjatë.

RAM matet në megabajt (MB) ose gigabajt (GB). Sa më shumë që keni, aq më shumë mund të bëni në të njëjtën kohë. Nëse nuk keni RAM të mjaftueshme, mund të vëreni se kompjuteri juaj është i ngadaltë kur keni disa programe të hapura. Për shkak të kësaj, shumë njerëz shtojnë RAM shtesë në kompjuterët e tyre për të përmirësuar performancën.</h4>
</div>
<div class="site6">
    <h1>Hard drive</h1>
    <img src="hd.jpg" alt="">
    <h4>Hard drive është vendi ku ruhen programet, dokumentet dhe skedarët e tjerë. Hard drive është ruajtje afatgjatë, që do të thotë që të dhënat ruhen akoma edhe nëse e ndizni kompjuterin ose hiqeni nga priza.

Hard drive mbi RAM kur ju drejtuar një program ose hapur një skedar. Kur ruani një skedar, të dhënat kopjohen përsëri në hard drive. Sa më shpejt të jetë hard drive, aq më shpejt kompjuteri mund të fillojë dhe të ngarkojë programe.</h4>
</div>
